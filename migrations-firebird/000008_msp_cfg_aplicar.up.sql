-- Migración 000008: tablas de configuración del mapeo "aplicar venta"
-- ============================================================================
-- Configuración EDITABLE (sin redeploy) del mapeo MSP_VENTAS → Microsip que
-- usa POST /ventas/{id}/aplicar. Son tablas de referencia: estructura + datos
-- semilla, sin lógica en la BD (sin defaults, sin triggers; los IDs/valores
