-- catálogos reales de Microsip (mismos IDs en producción, misma instalación).
--
--   MSP_CFG_ZONA_CAJA            zona del cliente → caja + cajero (por ruta)
--   MSP_CFG_FRECUENCIA_FORMA_PAGO frecuencia de pago → FORMA_DE_PAGO (lista)
--   MSP_CFG_PLAZO_CREDITO         plazo en meses   → CREDITO_EN_MESES (lista)
--   MSP_CFG_NUM_VENDEDORES        # vendedores     → NUMERO_DE_VENDEDORES (lista)
--   MSP_CFG_APLICAR               fila única: sucursal + formas de cobro
-- ============================================================================

-- ─── Tablas ──────────────────────────────────────────────────────────────────
CREATE TABLE MSP_CFG_ZONA_CAJA (
  ZONA_CLIENTE_ID INTEGER NOT NULL,
  CAJA_ID         INTEGER NOT NULL,
  CAJERO_ID       INTEGER NOT NULL,
  CONSTRAINT PK_MSP_CFG_ZONA_CAJA PRIMARY KEY (ZONA_CLIENTE_ID)
);

CREATE TABLE MSP_CFG_FRECUENCIA_FORMA_PAGO (
  FRECUENCIA       VARCHAR(15) CHARACTER SET ASCII NOT NULL,
  FORMA_DE_PAGO_ID INTEGER NOT NULL,
  CONSTRAINT PK_MSP_CFG_FREC_FP PRIMARY KEY (FRECUENCIA)
);

CREATE TABLE MSP_CFG_PLAZO_CREDITO (
  PLAZO_MESES         INTEGER NOT NULL,
  CREDITO_EN_MESES_ID INTEGER NOT NULL,
  CONSTRAINT PK_MSP_CFG_PLAZO_CREDITO PRIMARY KEY (PLAZO_MESES)
);

CREATE TABLE MSP_CFG_NUM_VENDEDORES (
  NUM_VENDEDORES          INTEGER NOT NULL,
  NUMERO_DE_VENDEDORES_ID INTEGER NOT NULL,
  CONSTRAINT PK_MSP_CFG_NUM_VENDEDORES PRIMARY KEY (NUM_VENDEDORES)
);

CREATE TABLE MSP_CFG_APLICAR (
  ID                     INTEGER NOT NULL,
  SUCURSAL_ID            INTEGER NOT NULL,
  FORMA_COBRO_CONTADO_ID INTEGER NOT NULL,
  FORMA_COBRO_CREDITO_ID INTEGER NOT NULL,
  CONSTRAINT PK_MSP_CFG_APLICAR PRIMARY KEY (ID),
  CONSTRAINT CK_MSP_CFG_APLICAR_SINGLETON CHECK (ID = 1)
);
COMMIT;

